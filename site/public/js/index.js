// Romance

function abrirModal(){
    const modal = document.getElementById('div_livro_detalhado')
    modal.classList.add('abrir')

    modal.addEventListener('click', (e) => {
        if(e.target.id == 'fechar' || e.target.id == 'div_livro_detalhado'){
            modal.classList.remove('abrir')
        }
    } ) 
}

